// let // let  es una variable que se puede modificar y es la forma recomendada de declarar variables
// se puede declarar en cualquier parte del codigo y se puede modificar
// se recomienda usar let en lugar de var
// 

let helloWord2 = "hello mundo 2 !"
console.log(helloWord2)

helloWord2 = "hola mundo 2, otra vez 2"
console.log(helloWord2)

// const  esta variable es constante no se puede modificar el valor
// se recomienda usar const para variables que no van a cambiar
// se debe declarar al inicio del codigo y no se puede modificar
// si se intenta modificar el valor de una variable const se genera un error
// se recomienda usar const para variables que no van a cambiar

const helloWord3 = "hello mundo 3 !"
console.log(helloWord3)

